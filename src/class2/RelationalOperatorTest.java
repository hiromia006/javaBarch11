package class2;

public class RelationalOperatorTest {
    public static void main(String[] args) {
        int mark1=98, mark2=87;
        System.out.println(mark1 > mark2);
        System.out.println(mark1 >= mark2);
        System.out.println(mark1 < mark2);
        System.out.println(mark1 <= mark2);
        System.out.println(mark1 != mark2);
        System.out.println(mark1 == mark2);
    }
}
