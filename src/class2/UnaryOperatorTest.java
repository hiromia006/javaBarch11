package class2;

public class UnaryOperatorTest {
    public static void main(String[] args) {
        int count=100;
        System.out.println(count++);//100,
        System.out.println(++count);//102
        System.out.println(--count);//101
        System.out.println(count--); //101
        System.out.println(count);//100

    }
}
